						</el-breadcrumb>
					</div>
					<h1>可惜！外媒PC Gamer盘点了《赛博朋克2077》砍掉的9个功能</h1>
					<p class="time">发布时间：2020-12-17 10:39:06</p>
					<div style="clear: both;"></div>
					<el-divider><i class="el-icon-reading"></i></el-divider>
				</div>
				<div class="mainText">
					<p>通往夜之城的道路是漫长的，经过7年的构思和发展，《赛博朋克2077》注定是要发生改变的。现在《赛博朋克2077》已经发售，如果回过头来看看CDPR在之前的采访和宣传中谈到了什么，有哪些功能没有被加入，这将是一个非常有意思的事情。</p>
					<div class="img">
						<img width="700px" src="../assets/news/new4-1.jpg">
					</div>
					<p>然而，其他的变化更引人注目，显示出CDPR必须重新设计，这些功能要么是不起作用，要么是过于雄心勃勃难以产出。以下是《赛博朋克2077》随着时间的推移所发生的一些主要变化。</p>
					<P><b>1.背景故事变成了出身</b></p>
					<div class="img">
						<img width="700px" src="../assets/news/new4-2.jpg">
					</div>
					<p>2018年的演示Demo，CDPR让玩家从三个不同类别的三个不同背景故事中做出选择，三个不同类别是：童年英雄，关键人生活动和为什么来到夜之城。这些选择将影响其他角色对你的反应。当CDPR再次于2020年展示游戏时，这些选择变成了三个出身：企业员工，流浪者和街头小子。</p>
					<p>设计师Mateusz Tomaszkiewicz在接受VG 247采访时表示：“让玩家通过一系列的选择来塑造V的背景故事，比如选择你的童年英雄、关键的人生事件，以及选择去夜城的原因，是我们在角色创建的开发早期就在研究和迭代的想法之一。然而，我们谈论的时间越长，就越觉得要让这些选择更有意义，更有影响力。这就是我们如何想到生命之路（出身）的想法......我们的目标是让玩家拥有尽可能多的自定义和角色扮演的选择，同时也为V这个角色充实这些完全不同的赛博朋克2077世界的视角。我认为生命之路系统就是帮助我们做到了这一点。"</p>
					<P><b>2.由于设计原因，贴墙跑被移除</b></p>
					<p>2018年的演示中，V有能力在墙壁上奔跑，然后用螳螂刀夹住。在2020年接受Gamereactor采访时，CD Projekt Red的关卡设计师Max Pears表示："这是我们由于设计原因删除的东西，但在你的移动方式上仍然会有很大的灵活性，这是肯定的。"</p>
					<P><b>3.酸雨曾被提到，但从来没有展示过</b></p>
					<p>在2019年的一次采访中，UI设计师Alivn Liu谈到了《赛博朋克》的天气系统，确认它会有动态天气。他说："我们也有酸雨，夜之城是一个污染非常严重的城市，我们也在探索这种东西，污染和全球变暖以及一切。”</p>
					<p>当被问及开始下酸雨NPC是否会散开时，他回答说："是的，在《巫师3》中也有这种情况。当雨来的时候，人们会寻求庇护。一般来说，我们是想让NPC非常可信，我们希望让人觉得这是一个人们真正生活的世界，这会让玩家们也想在这个世界中度过一段时间。"</p>
					<p>而在发售版本中，或许夜之城的雨对市民们来说是不健康的，但他们并没有用躲避来应对。在下雨时NPC们仍然在大街上行走，就和晴天时一样。</p>
					<P><b>4.地铁曾在预告片中出现，但CDPR从未确认可以使用。</b></p>
					<p>后来在2020年开发者Miles Tost确认玩家无法乘坐地铁，快速旅行系统将与整个城市的旅游公告板绑定。通过点击其中一个公告板，你可以通过某种形式的公共交通工具瞬间传送到另一个公告板。</p>
					<p>其中一位知情人士向彭博社透露，索尼最新的出货量预测仍远高于760万，不过已经没有早些时候那么高了。现在仍然有火车在夜之城穿行，甚至还有火车停靠站——但你不能进入它们，因为它们被一个障碍物挡住了，它会提示你选择一个快速的旅行目的地。火车纯粹是为了消遣。</p>
					<P><b>5.警察可以是腐败的，NPC将会对你屠杀平民做出反应</b></p>
					<p>《赛博朋克2077》的警察系统受到了严厉的批评。如果你犯罪，警察就会出现在你的旁边。你可以看着它发生。该系统并不取决于于你的犯罪行为是否被目击，警察也不会使用车辆来接近你的位置或进行追捕。他们就这么冒出来了。</p>
					<p>在2019年的一次采访中，CD Projekt Red的Alvin Liu含糊地描述了一种更有野心的对付警察的方法，但并没有提到他们实际上会如何攻击玩家。</p>
					<p>他说：“警察基本上是雇来的。法律的存在是为了接受公司的贿赂。所以一家公司可能会通过一项法律，说你不能再卖药了，而他们要执行这项法律。他们之所以能通过那条法律，唯一的原因就是他们贿赂了政府，他们只是把政府当做一个代理人。所以这不是一个你一定要相信政府的地方。可能也有一些好人在那里，但这是一个都在努力往上爬的城市。</p>
					<p>我们有一个我们仍然在迭代的系统。如果你杀很多人，人们不会对你好。有些人你是不能杀的，因为那可能会阻碍任务的进行，这只是设计上的，但这不是我们游戏的基础。所以我会把它比作《巫师3》，如果你在荒郊野外砍掉一个村民的头，守卫不会突然出现。但如果你在一个大镇子里，守卫看到你，附近的人尖叫着逃跑求救，人们就会来试图阻止你，他们通常会很强大。”</p>
					<p>在最终的游戏中，即使没有目击者，警察也会来追杀你，而且杀死平民并不会影响NPC对你的态度，这一点我们已经看到了。</p>
					<p><b>6.载具自定义</b></p>
					<p>载具自定义从来都不是一个大力宣传的功能，但在E3 2019的采访中，设计师Miles Tost说：“会有一些定制选项。对于摩托车来说，他们在这个时间点上相当有限，然而我们一直在寻找扩大我们的武器库的方法”。一年后，在E3 2020上，另外一个采访确认车辆不能被自定义。</p>
					<p><b>7.为V量身定制的广告会把你引向自动售货机</b></p>
					<p>在E3 2018的演示中，CDPR展示了一个广告，当V看的时候，广告会发生变化，推荐你去附近的自动售货机购买其产品。CDPR人员在演示中解释说：“我们RPG游戏体验的一个重要部分是拥有一个互动的世界。在这里，一个普通的广告已经变成了一个专门为V量身定做的广告，告知她去最近的自动售货机购买产品。在这种情况下，它告诉我们在哪里可以购买一些尼古拉汽水。”</p>
					<p>而在最后的游戏中，广告仍然是静态的。</p>
					<p><b>8.V可以使用一个线缆骇入个体敌人进入他们的网络</b></p>
					<p>骇入的原始设计要复杂得多，至少在演示中是这样。2018年E3展的演示中，V直接将插孔插入一个昏迷的Maelstrom帮派成员的头骨，从而进入一个精心的网络地图，里面有整个建筑和帮派基础设施。在最终的游戏中，V可以插口进入大多数终端机来偷钱和快速破解锻造用品，或者进入笔记本电脑来关闭安保系统，但仅此而已。</p>
					<p>他的扫描工具可以让他快速破解摄像头、炮塔和敌人，有效地看到特定建筑的基础设施。只是不要指望一个插孔动画或花哨的对本地网络的数字解读。</p>
					<p><b>9.一些曾经是第三人称的过场动画</b></p>
					<p>如果说曾几何时《赛博朋克2077》是一款像《巫师》一样的第三人称游戏，那么CDPR并没有将其展示出来。CDPR从未展示过像《上古卷轴》游戏中那样可以切换视角的选项--但在其2018年的游戏演示中，《赛博朋克2077》确实有一些第三人称的过场动画。在这样的场景中，玩家不用照镜子就能看到自己定制的V。</p>
					<p>到2019年，CDPR已经决定致力于“100%第一人称”，以帮助实现“完全沉浸”。剩下的一个例外是开车，它可以让你切换到第三人称。</p>
